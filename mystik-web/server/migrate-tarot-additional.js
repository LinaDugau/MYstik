function updateAdditionalInterpretations() {
  const db = getDatabase();
  
  console.log('Обновляем дополнительные толкования таро...');
  
  try {
    let updateCount = 0;
    
    // Обновляем толкования для расклада "Принятие решения"
    for (const [spreadId, positions] of Object.entries(ADDITIONAL_INTERPRETATIONS)) {
      console.log(`Обновляем расклад: ${spreadId}`);
      
      for (const [positionIndex, cards] of Object.entries(positions)) {
        for (const [cardId, interpretation] of Object.entries(cards)) {
          const result = db.prepare(`
            UPDATE tarot_interpretations 
            SET interpretation = ? 
            WHERE card_id = ? AND spread_id = ? AND position_index = ?
          `).run(interpretation, cardId, spreadId, parseInt(positionIndex));
          
          if (result.changes > 0) {
            updateCount++;
          }
        }
      }
    }
    
    console.log(`✓ Обновлено дополнительных толкований: ${updateCount}`);
    console.log('Обновление дополнительных толкований завершено успешно!');
    
    // Проверяем результат
    const interpretationCount = db.prepare('SELECT COUNT(*) as count FROM tarot_interpretations').get();
    console.log(`Всего толкований в базе: ${interpretationCount.count}`);
    
  } catch (error) {
    console.error('Ошибка при обновлении дополнительных толкований:', error);
    process.exit(1);
  }
}

// Запускаем обновление
updateAdditionalInterpretations();